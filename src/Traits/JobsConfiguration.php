<?php

namespace Helldar\Notifex\Traits;

trait JobsConfiguration
{
    /**
     * The number of seconds the job can run before timing out.
     *
     * @var int
     */
    public $timeout = 600;

    /**
     * The number of times the job may be attempted.
     *
     * @var int
     */
    public $tries = 10;
}
